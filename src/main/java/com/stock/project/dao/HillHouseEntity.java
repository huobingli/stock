package com.stock.project.dao;

public class HillHouseEntity {
    private Integer id;
    private String stock;
    private String name;
    private String time;
    private Integer amount;
    private Integer buycount;
    private String buyway;
    private String buyprice;
    private String buyallmoney;
    private String rate;
    private String racetrack;
    private String industry;
    private String remarks;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Integer getAmount() {
        return amount;
    }

    public void setAmount(Integer amount) {
        this.amount = amount;
    }

    public Integer getBuycount() {
        return buycount;
    }

    public void setBuycount(Integer buycount) {
        this.buycount = buycount;
    }

    public String getBuyway() {
        return buyway;
    }

    public void setBuyway(String buyway) {
        this.buyway = buyway;
    }

    public String getBuyprice() {
        return buyprice;
    }

    public void setBuyprice(String buyprice) {
        this.buyprice = buyprice;
    }

    public String getBuyallmoney() {
        return buyallmoney;
    }

    public void setBuyallmoney(String buyallmoney) {
        this.buyallmoney = buyallmoney;
    }

    public String getBuyrate() {
        return rate;
    }

    public void setBuyrate(String buyrate) {
        this.rate = buyrate;
    }

    public String getRacetrack() {
        return racetrack;
    }

    public void setRacetrack(String racetrack) {
        this.racetrack = racetrack;
    }

    public String getIndustry() {
        return industry;
    }

    public void setIndustry(String industry) {
        this.industry = industry;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
